CREATE TABLE locations (
    google_place_id VARCHAR(255) NOT NULL,
    google_maps_url VARCHAR(255) NOT NULL,
    crm_id VARCHAR(255) NULL DEFAULT NULL,
    analyzer_id VARCHAR(255) NULL DEFAULT NULL,
    display_name VARCHAR(255) NOT NULL,
    phone VARCHAR(255) NULL DEFAULT NULL,
    email VARCHAR(255) NULL DEFAULT NULL,
    website VARCHAR(255) NULL DEFAULT NULL,
    formatted_address VARCHAR(255) NOT NULL,
    street VARCHAR(255) NULL DEFAULT NULL,
    city VARCHAR(255) NULL DEFAULT NULL,
    state VARCHAR(255) NOT NULL,
    zip VARCHAR(255) NOT NULL,
    country VARCHAR(255) NOT NULL,
    latitude FLOAT NOT NULL,
    longitude FLOAT NOT NULL,
    rating FLOAT NULL DEFAULT NULL,
    rating_count INT NULL DEFAULT NULL,
    opening_hours TEXT NULL DEFAULT NULL,
    type VARCHAR(255) NULL DEFAULT NULL,
    types TEXT NULL DEFAULT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
